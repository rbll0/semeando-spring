package com.semando.ltda.domains.enums;

/**
 * Enumeração que representa os níveis de dificuldade dos quizzes no sistema.
 * <p>
 * Cada nível de dificuldade permite ao sistema oferecer uma experiência progressiva, ajustando a complexidade
 * das questões para engajar e desafiar os usuários em temas de sustentabilidade.
 * </p>
 * As dificuldades disponíveis são:
 * <ul>
 *     <li><b>FACIL</b> - Nível inicial, voltado para introduzir o tema ao usuário.</li>
 *     <li><b>MEDIO</b> - Nível intermediário, para usuários com conhecimento básico.</li>
 *     <li><b>DIFICIL</b> - Nível avançado, destinado a usuários com bom conhecimento.</li>
 * </ul>
 */

public enum Dificuldade {
    FACIL, MEDIO, DIFICIL
}
